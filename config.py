layer2Color = "yellow"   # Цвет второго скрытого слоя (желтый)
layer3Color = "blue"     # Цвет третьего скрытого слоя (синий)
outputNColor = "cyan"    # Цвет выходного слоя (голубой)

# ==================== ОТЛАДОЧНЫЕ НАСТРОЙКИ ====================

screenPrint = True       # Включает/выключает вывод отладочных сообщений в консоль


def tprint(message):
    """
    Условная печать сообщений в консоль.
    Выводит сообщение только если screenPrint = True.
    
    Аргументы:
        message: сообщение для вывода (строка или любой объект)
    
    Пример:
        tprint("Начало обучения")  # Выведет сообщение только если screenPrint = True
    """
    if screenPrint:
        print(message)


def getNodePosition(totalColumns, currentRowCount, layerNumber):
    """
    Рассчитывает координаты для отображения узлов (нейронов) на холсте.
    
    Формула расположения:
    - Нейроны располагаются в несколько колонок (по количеству слоев)
    - В каждой колонке нейроны равномерно распределены по вертикали
    
    Аргументы:
        totalColumns (int): Общее количество слоев в сети (входной + скрытые + выходной)
        currentRowCount (int): Количество нейронов в текущем слое
        layerNumber (int): Номер текущего слоя (0 - входной, 1 - первый скрытый и т.д.)
    
    Возвращает:
        list: Список координат [x, y] для каждого нейрона в слое.
              Формат: [[x1, y1], [x2, y2], ...]
    
    Пример:
        # Для слоя с 3 нейронами, который является 2-м по счету из 4 слоев
        positions = getNodePosition(4, 3, 2)
        # Вернет примерно: [[250, 200], [250, 400], [250, 600]]
    """
    
    # Вычисляем расстояние между колонками (слоями)
    # canvasWidth + 100 - добавляем отступ для лучшего распределения
    columnDistance = (canvasWidth + 100) / totalColumns
    
    # Вычисляем X-координату для текущего слоя
    # layerNumber * columnDistance - позиция по горизонтали
    # networkOffset - корректировка для центрирования
    x = layerNumber * columnDistance + networkOffset
    
    # Вычисляем вертикальный шаг между нейронами в слое
    # Делим высоту холста на (количество нейронов + 2)
    # +2 дает отступы сверху и снизу
    yGap = canvasHeight / (currentRowCount + 2)
    
    # Создаем список координат для каждого нейрона в слое
    coordinatesList = []
    for i in range(currentRowCount):
        # Y-координата: отступ в 1 шаг, затем для каждого нейрона - свой отступ
        y = (i + 1) * yGap
        
        # X-координата корректируется на половину ширины узла
        # Это нужно, чтобы центр круга был точно в расчетной точке
        xAdjusted = x - nodeWidth / 2
        yAdjusted = y - nodeHeight / 2
        
        coordinatesList.append([xAdjusted, yAdjusted])
    
    return coordinatesList


# ==================== ДОПОЛНИТЕЛЬНЫЕ ПОЛЕЗНЫЕ ФУНКЦИИ ====================

def getLayerColor(layerIndex):
    """
    Возвращает цвет для слоя по его индексу.
    
    Аргументы:
        layerIndex (int): Номер слоя (0 - входной, 1,2,3 - скрытые, 4+ - выходной)
    
    Возвращает:
        str: Название цвета для использования в tkinter
    """
    layerColors = [
        inputNColor,    # 0: Входной слой
        layer1Color,    # 1: Первый скрытый слой
        layer2Color,    # 2: Второй скрытый слой
        layer3Color,    # 3: Третий скрытый слой
        outputNColor    # 4+: Выходной слой (и все остальные)
    ]
    
    if layerIndex < len(layerColors) - 1:
        return layerColors[layerIndex]
    else:
        return outputNColor


def validateSettings():
    """
    Проверяет корректность настроек.
    Выводит предупреждения, если какие-то значения могут вызвать проблемы.
    
    Возвращает:
        bool: True если все настройки корректны, False если есть проблемы
    """
    is_valid = True
    
    if canvasWidth < nodeWidth * 2:
        tprint(f"ПРЕДУПРЕЖДЕНИЕ: canvasWidth ({canvasWidth}) меньше удвоенной nodeWidth ({nodeWidth*2})")
        is_valid = False
    
    if canvasHeight < nodeHeight * 2:
        tprint(f"ПРЕДУПРЕЖДЕНИЕ: canvasHeight ({canvasHeight}) меньше удвоенной nodeHeight ({nodeHeight*2})")
        is_valid = False
    
    if windowWidth < canvasWidth:
        tprint(f"СОВЕТ: windowWidth ({windowWidth}) меньше canvasWidth ({canvasWidth})")
        tprint("Возможно, часть холста не будет видна")
    
    if windowHeight < canvasHeight:
        tprint(f"СОВЕТ: windowHeight ({windowHeight}) меньше canvasHeight ({canvasHeight})")
        tprint("Возможно, часть холста не будет видна")
    
    return is_valid


# Проверка настроек только при прямом запуске файла
if __name__ == "__main__":
    validateSettings()